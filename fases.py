	def load_map(self, level_number):
		maze = self.game_levels[level_number]
		walls = []
		for i in range(len(maze)):
			for j in range(len(maze[0])):
				if maze[i][j] == '#':
					wall = Wall((j*32, i*32))
					walls.append(wall)
		return walls
		
	def draw_map(self, display, walls):
		for wall in walls:
			display.blit(wall.img, (wall.position))
	
